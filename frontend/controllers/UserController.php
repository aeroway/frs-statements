    /**
     * Updates an existing User model.
     * If update is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionUpdateAddress($id)
    {
        $model = $this->findModel($id);

        if (!($model->id === Yii::$app->user->identity->id)) {
            throw new ForbiddenHttpException('Вы не можете редактировать чужие записи.');
        }

        if ($model->load(Yii::$app->request->post())) {
            if ($model->subdivision_id != Yii::$app->user->identity->subdivision_id) {
                throw new ForbiddenHttpException('Адрес некорректен.');
            }

            $model->updated_at = time();

            if ($model->save()) {
                return $this->redirect(['site/index']);
            }
        }

        return $this->render('update-address', [
            'model' => $model,
        ]);
    }

    /**
     * Finds the User model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return User the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = User::findOne($id)) !== null) {
            return $model;
        }

        throw new NotFoundHttpException('The requested page does not exist.');
    }
